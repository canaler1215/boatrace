DEFAULT_COURSE_WIN_RATE: dict[int, float] = {
    1: 0.5524, 2: 0.1416, 3: 0.1275, 4: 0.1028, 5: 0.0586, 6: 0.0313,
}


def add_stadium_course_features(df: pd.DataFrame) -> pd.DataFrame:
    """
    場 × コース（boat_no）の 1 着率を course_win_rate 列として追加する。
    STADIUM_COURSE_WIN_RATE のテーブルを参照、未登録は DEFAULT_COURSE_WIN_RATE。
    """
    df = df.copy()

    def _lookup(row: pd.Series) -> float:
        sid = row.get("stadium_id")
        bn = row.get("boat_no")
        try:
            sid_int = int(sid)
            bn_int = int(bn)
        except (TypeError, ValueError):
            return DEFAULT_COURSE_WIN_RATE.get(int(bn) if pd.notna(bn) else 1, 0.0)
        per_stadium = STADIUM_COURSE_WIN_RATE.get(sid_int)
        if per_stadium is not None and bn_int in per_stadium:
            return per_stadium[bn_int]
        return DEFAULT_COURSE_WIN_RATE.get(bn_int, 0.0)

    df["course_win_rate"] = df.apply(_lookup, axis=1).astype(float)
    return df
